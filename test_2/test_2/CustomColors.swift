import UIKit
import Foundation

class CustomColors {
}

extension UIColor {
    public static var greyRailRoad: UIColor! {
        return UIColor(red: 51 / 255, green: 51 / 255, blue: 51 / 255, alpha: 1)
    }

    public static var yellowRailRoad: UIColor! {
        return UIColor(red: 248 / 255, green: 204 / 255, blue: 70 / 255, alpha: 1)
    }
}